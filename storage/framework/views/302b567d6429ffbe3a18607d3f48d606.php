    <script>
    function showEmployersSection() {
        var employersSection = document.getElementById('forEmployersSection');
        var institutionsSection = document.getElementById('forInstitutionsSection');
        var btnEmployers = document.getElementById('btnForEmployers');
        var btnInstitutions = document.getElementById('btnForInstitutions');
        var mainTitle = document.getElementById('mainSectionTitle');
        var mainSubtitle = document.getElementById('mainSectionSubtitle');
        
        if (employersSection && institutionsSection) {
            employersSection.style.display = 'block';
            institutionsSection.style.display = 'none';
            
            if (btnEmployers) btnEmployers.classList.add('active');
            if (btnInstitutions) btnInstitutions.classList.remove('active');
            
            if (mainTitle) mainTitle.textContent = 'For Educators';
            if (mainSubtitle) mainSubtitle.textContent = 'Follow the steps and apply for verified school jobs.';
        }
    }
    
    function showInstitutionsSection() {
        var employersSection = document.getElementById('forEmployersSection');
        var institutionsSection = document.getElementById('forInstitutionsSection');
        var btnEmployers = document.getElementById('btnForEmployers');
        var btnInstitutions = document.getElementById('btnForInstitutions');
        var mainTitle = document.getElementById('mainSectionTitle');
        var mainSubtitle = document.getElementById('mainSectionSubtitle');
        
        if (employersSection && institutionsSection) {
            employersSection.style.display = 'none';
            institutionsSection.style.display = 'block';
            
            if (btnEmployers) btnEmployers.classList.remove('active');
            if (btnInstitutions) btnInstitutions.classList.add('active');
            
            if (mainTitle) mainTitle.textContent = 'For Institutions';
            if (mainSubtitle) mainSubtitle.textContent = 'Smart Hiring. Verified Educators. Faster Recruitment.';
        }
    }
    
    // Ensure default view is Employers on page load
    document.addEventListener('DOMContentLoaded', function() {
        showEmployersSection();
    });
